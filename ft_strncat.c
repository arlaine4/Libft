#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t i;
	size_t j;
	size_t p;

	p = n;
	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (src[j] && (j < n))
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
