#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*str;
	int		size;

	size = 0;
	while (s[size])
		size++;
	if (!(str = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
